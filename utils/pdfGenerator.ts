import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';

export const generatePDF = (data: any, symptoms: string[]) => {
  const doc = new jsPDF();
  
  // Header
  doc.setFontSize(22);
  doc.setTextColor(59, 130, 246); // Blue-600
  doc.text('HealthAI Medical Report', 20, 20);
  
  doc.setFontSize(10);
  doc.setTextColor(100);
  doc.text(`Generated on: ${new Date(data.timestamp).toLocaleString()}`, 20, 28);
  doc.line(20, 32, 190, 32);

  // Patient Results
  doc.setFontSize(16);
  doc.setTextColor(0);
  doc.text('Assessment Summary', 20, 45);
  
  autoTable(doc, {
    startY: 50,
    head: [['Field', 'Details']],
    body: [
      ['Predicted Disease', data.disease],
      ['Confidence Score', `${(data.confidence * 100).toFixed(1)}%`],
      ['Risk Level', data.risk_level],
    ],
    theme: 'striped',
    headStyles: { fillColor: [59, 130, 246] },
  });

  // Symptoms
  doc.setFontSize(14);
  doc.text('Symptoms Reported', 20, (doc as any).lastAutoTable.finalY + 15);
  doc.setFontSize(10);
  doc.text(symptoms.join(', '), 20, (doc as any).lastAutoTable.finalY + 22, { maxWidth: 170 });

  // Recommendations
  doc.setFontSize(14);
  doc.text('Recommendations', 20, (doc as any).lastAutoTable.finalY + 40);
  
  autoTable(doc, {
    startY: (doc as any).lastAutoTable.finalY + 45,
    head: [['Precautions', 'Dietary Suggestions']],
    body: [
      [
        data.precautions ? data.precautions.join('\n') : 'Consult a doctor',
        data.diet ? data.diet.join('\n') : 'Maintain balanced diet'
      ]
    ],
    theme: 'grid',
    headStyles: { fillColor: [16, 185, 129] }, // Green-500
  });

  // Footer Disclaimer
  doc.setFontSize(8);
  doc.setTextColor(150);
  doc.text('Disclaimer: This report is generated by an AI system for informational purposes only.', 20, 280);
  doc.text('It is not a substitute for professional medical diagnosis or treatment.', 20, 285);

  doc.save(`HealthAI_Report_${data.disease}.pdf`);
};
